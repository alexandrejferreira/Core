namespace MikoPBX\PBXCoreREST\Controllers\Sysinfo;

use MikoPBX\Common\Providers\BeanstalkConnectionWorkerApiProvider;
use MikoPBX\PBXCoreREST\Controllers\BaseController;
use MikoPBX\PBXCoreREST\Http\Response;

/**
 * Class GetController
 *
 * This controller handles the requests related to system information.
 *
 * @example
 * /pbxcore/api/sysinfo/{name} Get system logs (GET)
 *
 * Get system information
 *   curl http://127.0.0.1/pbxcore/api/sysinfo/getInfo;
 *
 * Get external IP address:
 *   curl http://127.0.0.1/pbxcore/api/sysinfo/getExternalIpInfo
 *
 */
class GetController extends BaseController
{
    /**
     * Handles the call to different actions based on the action name
     *
     * @param string $actionName The name of the action
     */
    public function callAction(string $actionName): void
    {
        switch ($actionName) {
            case 'getInfo':
                $this->getInfoAction();
                break;
            default:
                $data = $this->request->getPost();
                $this->sendRequestToBackendWorker('sysinfo', $actionName, $data);
        }
    }

    /**
     * Parses the content of the file and includes it in the response
     *
     * This action retrieves the system information file generated by the backend worker,
     * reads its content, and includes it in the response payload.
     */
    private function getInfoAction(): void
    {
        // Prepare the request message
        $requestMessage = json_encode(
            [
                'processor' => 'sysinfo',
                'data'      => $this->request->getPost(),
                'action'    => 'getInfo',
            ]
        );

        // Send the request to the backend worker
        $connection     = $this->di->getShared(BeanstalkConnectionWorkerApiProvider::SERVICE_NAME);
        $response       = $connection->request($requestMessage, 30, 0);
        if ($response !== false) {
            $response = json_decode($response, true);
            $filename = $response['data']['filename'] ?? '';
            if ( ! file_exists($filename)) {
                $response['messages'][] = 'System information collected file not found';
            } else {
                // Read the content of the file and include it in the response
                $response['data']['content']  = '' . file_get_contents($filename);
                unlink($filename); // Remove the temporary file
            }
            $this->response->setPayloadSuccess($response);
        } else {
            $this->sendError(Response::INTERNAL_SERVER_ERROR);
        }
    }
}